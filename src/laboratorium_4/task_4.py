# Krotka (ang. tuple) w Pythonie to struktura danych, która jest uporządkowaną sekwencją elementów.
# Krotki są podobne do list, ale mają kilka kluczowych różnic:
# Cechy krotek:
# 1. Niezmienność - po utworzeniu krotki nie można zmieniać, dodawać ani usuwać jej
# elementów. Oznacza to, że krotka jest niezmienialna, co odróżnia ją od listy, która jest
# zmienialna.
# 2. Uporządkowanie - elementy krotki mają swoje miejsce, czyli każdemu elementowi
# przypisany jest indeks (numer porządkowy), który zaczyna się od 0.
# 3. Mogą zawierać różne typy danych - w jednej krotce można przechowywać elementy
# różnych typów, np. liczby, ciągi znaków, inne krotki itp.
# 4. Zastosowanie nawiasów okrągłych - krotki są definiowane przy użyciu nawiasów okrągłych
# (), podczas gdy listy są definiowane przy użyciu nawiasów kwadratowych [].